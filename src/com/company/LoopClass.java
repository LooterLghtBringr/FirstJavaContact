package com.company;

public class LoopClass {

    public void ForOutput()
    {
        for (int i = 1; i<=100;i++)
        {
            System.out.println(i);
        }

    }

    public void WhileOutput()
    {
        int i = 1;
        while (i<=100)
        {
            System.out.println(i);
            i++;
        }
    }
}
